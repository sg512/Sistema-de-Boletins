<h1><i class="glyphicon glyphicon-info-sign"></i> Perguntas Frequentes</h1>
<br>
<br>
<br>

<p>
    <h4><strong>1) -"Submeti uma pesquisa na página, preenchi todos os campos, me retornou <mark>"+ 0 aparição'(ões) no boletim ..."</mark> e um link que leva a um "BI", o que significa? </strong></h4>
    <br>- <mark>Resposta:</mark> Há pelo menos parte da palavra, por exemplo "teste", irá retornar a palavra "teste" sozinha com "n" aparições e "testemunho" com "0" aparições.
</p>
<br>
<p>
    <h4><strong>2) -"Pesquisei <mark>Maria de Lurdes</mark> e a página me retornou muitas aparições o que dificultou minha busca, o que acontece?"</strong></h4>
    <br>- <mark>Resposta:</mark> Evite fazer buscas contendo "monossílabas" (da, de,..), para o bom funcionamento pesquise somente "Maria Lurdes".
</p>
<br>
<p>
    <h4><strong>3) -"Copiei, colei um texto na pesquisa de boletins, cliquei em pesquisar e não me retornou nada."</strong></h4>
    <br>- <mark>Resposta:</mark> Evite "copiar e colar" algum texto no buscador, pois ao digitar espaço a página separa as palavras.
</p>